let fahrenheitValue = 32

// calculate celsius and store in celsius variable
let celsiusValue = (fahrenheitValue - 32) * 5 / 9
// print that value
console.log('Celsius value', celsiusValue)

// calculate kelvin value and store in variable
let kelvinValue = (fahrenheitValue - 459.67) * 5 / 9
// print that value
console.log('Kelvin value', kelvinValue)